public class ScoreW4 {

	private int value;
	private String dateOfScore;
	
	public ScoreW4(int value, String dateOfScore) {
		this.value = value;
		this.dateOfScore = dateOfScore;
	}

	public int getValue() {
		return value;
	}

	public String getDateOfScore() {
		return dateOfScore;
	}
	
}
